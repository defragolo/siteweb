<h1>Pourquoi faire des dons d'organes? </h1>

<p>En effectuant un don d'organe, il est possible de sauver une vie.La plupart des greffes sont vitales: Le nombre de demandeurs d'organes étant de plus en plus important, et le nombre de donneurs d'organes est insuffisant. En 2016, 22 617 personnes ont eu besoin d’une greffe avec seulement
5 891 organes greffés. </p>
 <h2> Quel est notre objectif?
</h2>
<p> Ce site web permet au personnel médical d'acheter des organes,notre objectif est d'optimiser la répartition d'organes aini que la disponibilité en augmentant la quantité.De plus en plus d'organes sont d'orignies animales ou artificielles,le but étant d'éffectuer des greffes avec un haut taux de réussite afin de minimiser le nombre de demandeurs en produisant des organes de bonne qualité.
</p>
</br>
<p>Nos organes sont de bonne qualité, sont leurs origines sont humaines, animales ou sont crées artificiellement. </p>
</br>
<p>ATTENTION!!! Ce site web n'est accesible qu'au personnel médical certifié, et est impliqué dans la vente  d'organes à des fins purement médicales.   .</p>
